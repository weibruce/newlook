package com.querer.libra.app.event.domain.model;

import java.util.Date;

import com.querer.libra.platform.core.domain.model.common.BaseModel;

public class EventModel extends BaseModel {

    /* fields -------------------------------------------------------------- */

    private String name;
    private String storeName;
    private String description;

    private Date startTime;
    private Date endTime;

    private Boolean enabled;
    private String openingCode;

    private String openId;

    /* getters/setters ----------------------------------------------------- */

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStoreName() {
        return storeName;
    }

    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public Boolean getEnabled() {
        return enabled;
    }

    public void setEnabled(Boolean enabled) {
        this.enabled = enabled;
    }

    public String getOpeningCode() {
        return openingCode;
    }

    public void setOpeningCode(String openingCode) {
        this.openingCode = openingCode;
    }

    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId;
    }
}
